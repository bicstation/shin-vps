export const getBuildEpisodes = (): Episode[] => {
  return Array.from({ length: 40 }, (_, i) => {
    const vol = i + 1;
    let phaseKey: keyof typeof BUILD_TITLES = 'phase1';
    if (vol <= 10) phaseKey = 'phase1';
    else if (vol <= 20) phaseKey = 'phase2';
    else if (vol <= 30) phaseKey = 'phase3';
    else phaseKey = 'phase4';

    const titleIdx = (vol - 1) % 10;
    return {
      volume: vol,
      title: `Vol.${vol}: ${BUILD_TITLES[phaseKey][titleIdx]}`,
      slug: `vol${vol}`,
      technicalHint: BUILD_HINTS[phaseKey],
      isFurnitureUnlocked: vol >= 31
    };
  });
};

export const BUILD_SERIES_DETAIL = {
  ...BTO_SERIES_CONFIG.build_logic,
  episodes: getBuildEpisodes()
};