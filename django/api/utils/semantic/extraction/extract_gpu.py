import re


# =========================================================
# GPU PATTERNS
# =========================================================

GPU_PATTERNS = [

    # =====================================================
    # RTX 50
    # =====================================================

    r"rtx[\s\-]?5090",

    r"rtx[\s\-]?5080",

    r"rtx[\s\-]?5070[\s\-]?ti",

    r"rtx[\s\-]?5070",

    # =====================================================
    # RTX 40
    # =====================================================

    r"rtx[\s\-]?4090",

    r"rtx[\s\-]?4080",

    r"rtx[\s\-]?4070[\s\-]?ti",

    r"rtx[\s\-]?4070",

    r"rtx[\s\-]?4060[\s\-]?ti",

    r"rtx[\s\-]?4060",

    r"rtx[\s\-]?4050",

    # =====================================================
    # RADEON
    # =====================================================

    r"radeon ai pro",

    r"radeon",

    # =====================================================
    # INTEL ARC
    # =====================================================

    r"intel arc",

    # =====================================================
    # INTEGRATED
    # =====================================================

    r"intel graphics",

    r"amd radeon graphics",
]


# =========================================================
# EXTRACT GPU
# =========================================================

def extract_gpu(

    source_text,

    trace_runtime=False,

):

    raw_tokens = []

    # =====================================================
    # MATCH
    # =====================================================

    for pattern in GPU_PATTERNS:

        matches = re.findall(

            pattern,

            source_text,

            re.IGNORECASE,

        )

        for match in matches:

            token = str(
                match
            ).strip()

            if token:

                raw_tokens.append(
                    token
                )

    # =====================================================
    # UNIQUE
    # =====================================================

    raw_tokens = list(
        set(raw_tokens)
    )

    return raw_tokens