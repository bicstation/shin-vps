'use client'

/* ============================================================================
🔥 Contracts
============================================================================ */

import type {
    SemanticRankingRuntime,
} from '../types/contracts'

/* ============================================================================
🔥 Components
============================================================================ */

import {
    RankingHero,
    FlagshipCard,
    ComparisonGrid,
    RankingList,
    RankingContinuation,
} from './'

/* ============================================================================
🔥 Props
============================================================================ */

type Props = {

    runtime: SemanticRankingRuntime

}

/* ============================================================================
🔥 Ranking Runtime
============================================================================ */

export default function RankingRuntime({

    runtime,

}: Props) {

    console.log(runtime)

    /* =========================================================================
    🔥 Runtime
    ========================================================================= */

    const products =
        runtime.data.products

    /* =========================================================================
    🔥 Ranking Structure
    ========================================================================= */

    const flagship =
        products[0]

    const comparisonProducts =
        products.slice(1, 4)

    const rankingProducts =
        products.slice(4)

    /* =========================================================================
    🔥 Render
    ========================================================================= */

    return (

        <main>

            {/* ==========================================================
            Hero
            ========================================================== */}

            <RankingHero

                runtime={runtime}

                totalProducts={products.length}

            />

            {/* ==========================================================
            Flagship
            ========================================================== */}

            {

                flagship && (

                    <FlagshipCard

                        product={flagship}

                        rank={1}

                    />

                )

            }

            {/* ==========================================================
            Comparison
            ========================================================== */}

            {

                comparisonProducts.length > 0 && (

                    <ComparisonGrid

                        products={comparisonProducts}

                        runtime={runtime}

                    />

                )

            }

            {/* ==========================================================
            Ranking
            ========================================================== */}

            {

                rankingProducts.length > 0 && (

                    <RankingList

                        products={rankingProducts}

                        startRank={5}

                    />

                )

            }

            {/* ==========================================================
            Continuation
            ========================================================== */}

            <RankingContinuation />

        </main>

    )

}