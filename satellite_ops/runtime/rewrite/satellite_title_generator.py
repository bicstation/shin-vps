def generate_satellite_title(topic, summary=""):


    raw_title = topic.get("title", "")

    # ------------------------------------------------------------------------
    # Normalize
    # ------------------------------------------------------------------------

    title = raw_title.strip()

    # ------------------------------------------------------------------------
    # Local LLM / Gemma
    # ------------------------------------------------------------------------

    if "Gemma" in title:
        return "ローカルLLM活用事例に注目"

    if "LLM" in title:
        return "ローカルAI活用の動き広がる"

    # ------------------------------------------------------------------------
    # NVIDIA / GPU / AI Infra
    # ------------------------------------------------------------------------

    if "NVIDIA" in title:
        return "AI開発向けGPU動向に注目集まる"

    if "GPU" in title:
        return "AI向けGPU需要に関心高まる"

    # ------------------------------------------------------------------------
    # MacBook / Local AI
    # ------------------------------------------------------------------------

    if "MacBook" in title:
        return "MacBookでローカルAI活用を試す動き"

    # ------------------------------------------------------------------------
    # Creator / Video Workflow
    # ------------------------------------------------------------------------

    if "動画" in title:
        return "AIによる動画整理活用に関心高まる"

    if "映像" in title:
        return "映像制作向けAI活用広がる"

    # ------------------------------------------------------------------------
    # AI General
    # ------------------------------------------------------------------------

    if "AI" in title:
        return "AI活用事例への関心広がる"

    # ------------------------------------------------------------------------
    # Cloud / Infrastructure
    # ------------------------------------------------------------------------

    if "Cloud" in title:
        return "クラウドAI活用競争が加速"

    # ------------------------------------------------------------------------
    # Default Fallback
    # ------------------------------------------------------------------------

    return title[:40]