# =========================================================
# Generate Semantic FAQ
# =========================================================

def generate_semantic_faq(
    attribute
):

    name = attribute.name

    attr_type = attribute.attr_type

    faqs = []


    # =====================================================
    # Usage
    # =====================================================

    if attr_type == "usage":

        faqs = [

            {
                "question":
                    f"{name}向けPCではGPU性能は重要ですか？",

                "answer":
                    f"{name}用途ではGPU性能が重要になる場合があります。"
                    f"特に高負荷処理では高性能GPU搭載PCがおすすめです。"
            },

            {
                "question":
                    f"{name}向けPCはメモリ容量どれくらい必要ですか？",

                "answer":
                    f"{name}用途では16GB以上のメモリが推奨されます。"
                    f"用途によっては32GB以上もおすすめです。"
            }
        ]


    # =====================================================
    # GPU
    # =====================================================

    elif attr_type == "gpu":

        faqs = [

            {
                "question":
                    f"{name}搭載PCはゲーム用途に向いていますか？",

                "answer":
                    f"{name}搭載PCは高性能GPUを必要とする"
                    f"ゲームやクリエイティブ用途に適しています。"
            },

            {
                "question":
                    f"{name}搭載PCはAI用途にも使えますか？",

                "answer":
                    f"{name}はAI・画像生成・動画編集など"
                    f"高負荷用途にも活用できます。"
            }
        ]


    # =====================================================
    # CPU
    # =====================================================

    elif attr_type == "cpu":

        faqs = [

            {
                "question":
                    f"{name}搭載PCの特徴は何ですか？",

                "answer":
                    f"{name}搭載PCは高速処理やマルチタスク性能に"
                    f"優れているモデルがあります。"
            },

            {
                "question":
                    f"{name}搭載PCはビジネス用途にも向いていますか？",

                "answer":
                    f"{name}はビジネス・クリエイティブ・開発用途など"
                    f"幅広い用途に対応できます。"
            }
        ]


    # =====================================================
    # Default
    # =====================================================

    else:

        faqs = [

            {
                "question":
                    f"{name}とは何ですか？",

                "answer":
                    f"{name}に関連するおすすめPCを"
                    f"比較できるランキングページです。"
            }
        ]


    return faqs