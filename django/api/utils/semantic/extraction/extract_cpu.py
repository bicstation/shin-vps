import re


# =========================================================
# CPU PATTERNS
# =========================================================

CPU_PATTERNS = [

    # =====================================================
    # INTEL CORE ULTRA
    # =====================================================

    r"core ultra\s?9",

    r"core ultra\s?7",

    r"core ultra\s?5",

    # =====================================================
    # INTEL CORE
    # =====================================================

    r"core i9",

    r"core i7",

    r"core i5",

    r"core i3",

    # =====================================================
    # AMD RYZEN
    # =====================================================

    r"ryzen ai",

    r"ryzen 9",

    r"ryzen 7",

    r"ryzen 5",

    r"ryzen 3",

    # =====================================================
    # ARM
    # =====================================================

    r"snapdragon x",

    # =====================================================
    # WORKSTATION
    # =====================================================

    r"threadripper",

    r"xeon",
]


# =========================================================
# EXTRACT CPU
# =========================================================

def extract_cpu(

    source_text,

    trace_runtime=False,

):

    raw_tokens = []

    # =====================================================
    # MATCH
    # =====================================================

    for pattern in CPU_PATTERNS:

        matches = re.findall(

            pattern,

            source_text,

            re.IGNORECASE,

        )

        for match in matches:

            token = str(
                match
            ).strip()

            if token:

                raw_tokens.append(
                    token
                )

    # =====================================================
    # UNIQUE
    # =====================================================

    raw_tokens = list(
        set(raw_tokens)
    )

    return raw_tokens