from satellite_ops.runtime.rewrite.rewrite_runtime import (
    rewrite_lightly,
)

# ============================================================================
# Rewrite Orchestrator
# ============================================================================


class RewriteOrchestrator:

    # ------------------------------------------------------------------------
    # Init
    # ------------------------------------------------------------------------

    def __init__(self):

        pass

    # ------------------------------------------------------------------------
    # Execute Rewrite
    # ------------------------------------------------------------------------

    def execute(

        self,

        article_text: str,

        persona: str = "",

    ) -> str:

        # --------------------------------------------------------------------
        # Validation
        # --------------------------------------------------------------------

        if not article_text:

            return ""

        # --------------------------------------------------------------------
        # Lightweight Rewrite
        # --------------------------------------------------------------------

        rewritten = rewrite_lightly(

            article_text,

            persona,
        )

        # --------------------------------------------------------------------
        # Fallback Continuity
        # --------------------------------------------------------------------

        if not rewritten:

            return article_text

        # --------------------------------------------------------------------
        # Cleanup
        # --------------------------------------------------------------------

        rewritten = rewritten.strip()

        # --------------------------------------------------------------------
        # Empty Protection
        # --------------------------------------------------------------------

        if not rewritten:

            return article_text

        # --------------------------------------------------------------------
        # Final
        # --------------------------------------------------------------------

        return rewritten