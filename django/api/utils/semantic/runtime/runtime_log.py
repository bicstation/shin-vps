import pprint

from datetime import datetime


# =========================================================
# RUNTIME LOG
# =========================================================

def runtime_log(

    enabled,

    title,

    payload=None,

):

    # =====================================================
    # DISABLED
    # =====================================================

    if not enabled:

        return

    # =====================================================
    # TIMESTAMP
    # =====================================================

    timestamp = datetime.now().strftime(
        "%Y-%m-%d %H:%M:%S"
    )

    # =====================================================
    # HEADER
    # =====================================================

    print()

    print(
        "=" * 64
    )

    print(
        f"[SEMANTIC RUNTIME] {title}"
    )

    print(
        f"[TIME] {timestamp}"
    )

    print(
        "=" * 64
    )

    # =====================================================
    # PAYLOAD
    # =====================================================

    if payload is None:

        print(
            "[EMPTY PAYLOAD]"
        )

        return

    # =====================================================
    # STRUCTURED
    # =====================================================

    if isinstance(

        payload,

        (
            dict,
            list,
            tuple,
            set,
        ),

    ):

        pprint.pprint(

            payload,

            sort_dicts=False,

            width=120,

            compact=False,
        )

    # =====================================================
    # STRING
    # =====================================================

    else:

        print(
            payload
        )

    # =====================================================
    # FOOTER
    # =====================================================

    print()

    print(
        "-" * 64
    )