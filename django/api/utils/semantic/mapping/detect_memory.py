# =========================================================
# MEMORY ATTRIBUTE DETECTION
# =========================================================

def detect_memory_attr(
    specs,
):

    slugs = []

    memory = specs.get(
        "memory_gb",
        0
    ) or 0

    # =====================================================
    # 64GB+
    # =====================================================

    if memory >= 64:

        slugs.append(
            "memory-64gb"
        )

    # =====================================================
    # 32GB+
    # =====================================================

    elif memory >= 32:

        slugs.append(
            "memory-32gb"
        )

    # =====================================================
    # 16GB+
    # =====================================================

    elif memory >= 16:

        slugs.append(
            "memory-16gb"
        )

    # =====================================================
    # 8GB+
    # =====================================================

    elif memory >= 8:

        slugs.append(
            "memory-8gb"
        )

    # =====================================================
    # LOW MEMORY
    # =====================================================

    else:

        slugs.append(
            "memory-basic"
        )

    return slugs