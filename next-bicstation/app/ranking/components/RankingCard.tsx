'use client'

/* ============================================================================
🔥 Next
============================================================================ */

import Link
    from 'next/link'

/* ============================================================================
🔥 Types
============================================================================ */

import type {

    RankingItem,

} from '../types/ranking'

/* ============================================================================
🔥 Styles
============================================================================ */

import styles
    from '../styles/ranking.module.css'

/* ============================================================================
🔥 Props
============================================================================ */

type Props = {

    item:

        RankingItem

}

/* ============================================================================
🔥 Ranking Card
============================================================================ */

export default function RankingCard({

    item,

}: Props) {

    return (

        <Link

            href={`/ranking/${item.slug}`}

            className={
                styles.rankingCard
            }

        >

            {/* ==========================================================
            Icon
            ========================================================== */}

            {

                item.icon && (

                    <div
                        className={
                            styles.rankingCardIcon
                        }
                    >

                        {item.icon}

                    </div>

                )

            }

            {/* ==========================================================
            Title
            ========================================================== */}

            <h3
                className={
                    styles.rankingCardTitle
                }
            >

                {

                    item.title

                    ||

                    item.name

                }

            </h3>

            {/* ==========================================================
            Description
            ========================================================== */}

            <p
                className={
                    styles.rankingCardDescription
                }
            >

                {

                    item.description

                    ||

                    'おすすめランキングをご覧いただけます。'

                }

            </p>

            {/* ==========================================================
            Meta
            ========================================================== */}

            <div
                className={
                    styles.rankingCardMeta
                }
            >

                {

                    item.product_count !== undefined && (

                        <span>

                            {item.product_count.toLocaleString()}

                            件の商品

                        </span>

                    )

                }

                {

                    item.attribute_count !== undefined && (

                        <span>

                            {item.attribute_count}

                            属性

                        </span>

                    )

                }

            </div>

            {/* ==========================================================
            Footer
            ========================================================== */}

            <div
                className={
                    styles.rankingCardFooter
                }
            >

                <span
                    className={
                        styles.rankingCardLink
                    }
                >

                    ランキングを見る →

                </span>

            </div>

        </Link>

    )

}