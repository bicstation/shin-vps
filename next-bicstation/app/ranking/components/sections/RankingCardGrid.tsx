'use client'

/* ============================================================================
🔥 Components
============================================================================ */

import RankingCard
    from './RankingCard'

/* ============================================================================
🔥 Types
============================================================================ */

import type {

    NavigationRuntimeItem,

} from '@/shared/lib/api/django/pc/navigation/contracts'

/* ============================================================================
🔥 Styles
============================================================================ */

import styles
    from '../../styles/sections/grid-section.module.css'

/* ============================================================================
🔥 Props
============================================================================ */

type Props = {

    items: NavigationRuntimeItem[]

}

/* ============================================================================
🔥 Ranking Card Grid
============================================================================ */

export default function RankingCardGrid({

    items,

}: Props) {

    /* =========================================================================
    🔥 Empty
    ========================================================================= */

    if (

        items.length === 0

    ) {

        return null

    }

    /* =========================================================================
    🔥 Render
    ========================================================================= */

    return (

        <div
            className={
                styles.rankingGrid
            }
        >

            {

                items.map(

                    item => (

                        <RankingCard

                            key={

                                item.slug

                            }

                            item={

                                item

                            }

                        />

                    )

                )

            }

        </div>

    )

}