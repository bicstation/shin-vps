def generate_satellite_title(topic, summary=""):


    raw_title = topic.get("title", "")

    # ------------------------------------------------------------------------
    # MacBook / Local AI
    # ------------------------------------------------------------------------

    if "MacBook" in raw_title:
        return "MacBookでローカルAI活用を試す動き"

    # ------------------------------------------------------------------------
    # NVIDIA
    # ------------------------------------------------------------------------

    if "NVIDIA" in raw_title:
        return "AI開発向けGPU動向に注目集まる"

    # ------------------------------------------------------------------------
    # Gemma
    # ------------------------------------------------------------------------

    if "Gemma" in raw_title:
        return "ローカルLLM活用事例に注目"

    # ------------------------------------------------------------------------
    # Video / Creator
    # ------------------------------------------------------------------------

    if "動画" in raw_title:
        return "AIによる動画整理活用に関心高まる"

    # ------------------------------------------------------------------------
    # AI Default
    # ------------------------------------------------------------------------

    if "AI" in raw_title:
        return "AI活用事例への関心広がる"

    # ------------------------------------------------------------------------
    # Fallback
    # ------------------------------------------------------------------------

    return raw_title[:40]

