import re


# =========================================================
# DISPLAY PATTERNS
# =========================================================

DISPLAY_PATTERNS = [

    # =====================================================
    # RESOLUTION
    # =====================================================

    r"4k",

    r"uhd",

    r"wqhd",

    r"qhd",

    r"full hd",

    r"fhd",

    # =====================================================
    # PANEL
    # =====================================================

    r"oled",

    r"qd-oled",

    r"ips",

    # =====================================================
    # REFRESH RATE
    # =====================================================

    r"240hz",

    r"144hz",

    r"120hz",

    r"60hz",

    # =====================================================
    # ASPECT
    # =====================================================

    r"21:9",

    r"32:9",
]


# =========================================================
# EXTRACT DISPLAY
# =========================================================

def extract_display(

    source_text,

    trace_runtime=False,

):

    raw_tokens = []

    # =====================================================
    # MATCH
    # =====================================================

    for pattern in DISPLAY_PATTERNS:

        matches = re.findall(

            pattern,

            source_text,

            re.IGNORECASE,

        )

        for match in matches:

            token = str(
                match
            ).strip()

            if token:

                raw_tokens.append(
                    token
                )

    # =====================================================
    # UNIQUE
    # =====================================================

    raw_tokens = list(
        set(raw_tokens)
    )

    return raw_tokens