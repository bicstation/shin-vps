# --------------------------------------------------
# Imports
# --------------------------------------------------

from visualization.common.constants import (
    SEPARATOR,
)

# --------------------------------------------------
# Blank
# --------------------------------------------------

def blank():
    print()

# --------------------------------------------------
# Line
# --------------------------------------------------

def line():
    print(SEPARATOR)

# --------------------------------------------------
# Title
# --------------------------------------------------

def title(
    text,
):

    blank()
    line()
    print(f" {text}")
    line()
    blank()

# --------------------------------------------------
# Section
# --------------------------------------------------

def section(
    text,
):

    print()
    print(text)
    print("-" * len(text))

# --------------------------------------------------
# Info
# --------------------------------------------------

def info(
    message,
):
    print(f"[INFO] {message}")

# --------------------------------------------------
# Success
# --------------------------------------------------

def success(
    message,
):
    print(f"[ OK ] {message}")

# --------------------------------------------------
# Warning
# --------------------------------------------------

def warning(
    message,
):
    print(f"[WARN] {message}")

# --------------------------------------------------
# Error
# --------------------------------------------------

def error(
    message,
):
    print(f"[ERROR] {message}")

# --------------------------------------------------
# Key / Value
# --------------------------------------------------

def key_value(
    key,
    value,
):
    print(f"{key:<12}: {value}")

# --------------------------------------------------
# Complete
# --------------------------------------------------

def complete():
    blank()
    success("Completed.")
    blank()