import re


# =========================================================
# GPU PATTERNS
# =========================================================

GPU_PATTERNS = [

    # =====================================================
    # RTX 50
    # =====================================================

    r"(rtx[\s\-]?5090)",

    r"(rtx[\s\-]?5080)",

    r"(rtx[\s\-]?5070[\s\-]?ti)",

    r"(rtx[\s\-]?5070)",

    # =====================================================
    # RTX 40
    # =====================================================

    r"(rtx[\s\-]?4090)",

    r"(rtx[\s\-]?4080)",

    r"(rtx[\s\-]?4070[\s\-]?ti)",

    r"(rtx[\s\-]?4070)",

    r"(rtx[\s\-]?4060[\s\-]?ti)",

    r"(rtx[\s\-]?4060)",

    r"(rtx[\s\-]?4050)",

    # =====================================================
    # RTX LAPTOP
    # =====================================================

    r"(geforce[\s\-]?rtx[\s\-]?5090)",

    r"(geforce[\s\-]?rtx[\s\-]?5080)",

    r"(geforce[\s\-]?rtx[\s\-]?5070)",

    r"(geforce[\s\-]?rtx[\s\-]?4070)",

    r"(geforce[\s\-]?rtx[\s\-]?4060)",

    r"(geforce[\s\-]?rtx[\s\-]?4050)",

    # =====================================================
    # RADEON
    # =====================================================

    r"(radeon[\s\-]?ai[\s\-]?pro)",

    r"(radeon)",

    # =====================================================
    # INTEL ARC
    # =====================================================

    r"(intel[\s\-]?arc)",

    # =====================================================
    # INTEGRATED
    # =====================================================

    r"(intel[\s\-]?graphics)",

    r"(amd[\s\-]?radeon[\s\-]?graphics)",
]


# =========================================================
# NORMALIZE GPU TOKEN
# =========================================================

def normalize_gpu_token(

    token,

):

    token = str(
        token
    ).lower().strip()

    # =====================================================
    # CLEANUP
    # =====================================================

    token = re.sub(
        r"\s+",
        " ",
        token,
    )

    # =====================================================
    # RTX NORMALIZATION
    # =====================================================

    token = token.replace(
        "geforce ",
        "",
    )

    token = token.replace(
        "-",
        " ",
    )

    # =====================================================
    # RETURN
    # =====================================================

    return token


# =========================================================
# EXTRACT GPU
# =========================================================

def extract_gpu(

    source_text,

    trace_runtime=False,

):

    raw_tokens = []

    # =====================================================
    # SOURCE
    # =====================================================

    source_text = str(
        source_text
    ).lower()

    # =====================================================
    # MATCH
    # =====================================================

    for pattern in GPU_PATTERNS:

        matches = re.findall(

            pattern,

            source_text,

            re.IGNORECASE,

        )

        for match in matches:

            token = normalize_gpu_token(
                match
            )

            if token:

                raw_tokens.append(
                    token
                )

    # =====================================================
    # UNIQUE
    # =====================================================

    raw_tokens = sorted(
        list(
            set(raw_tokens)
        )
    )

    return raw_tokens