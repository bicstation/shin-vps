'use client'

import {

    useState,

} from 'react'

/* ============================================================================
Types
============================================================================ */

import type {

    FinderRuntime,

} from '../types/finder'

/* ============================================================================
Hook
============================================================================ */

export function useFinderState() {

    /* ------------------------------------------------------------------------
    Intent
    ------------------------------------------------------------------------ */

    const [

        selectedIntent,

        setSelectedIntent,

    ] =

        useState<string | null>(

            null

        )

    /* ------------------------------------------------------------------------
    Budget
    ------------------------------------------------------------------------ */

    const [

        maxPrice,

        setMaxPrice,

    ] =

        useState<number | null>(

            null

        )

    /* ------------------------------------------------------------------------
    Runtime
    ------------------------------------------------------------------------ */

    const [

        runtime,

        setRuntime,

    ] =

        useState<FinderRuntime | null>(

            null

        )

    /* ------------------------------------------------------------------------
    UI State
    ------------------------------------------------------------------------ */

    const [

        loading,

        setLoading,

    ] =

        useState(

            false

        )

    const [

        error,

        setError,

    ] =

        useState<string | null>(

            null

        )

    /* ------------------------------------------------------------------------
    Reset
    ------------------------------------------------------------------------ */

    function reset() {

        setRuntime(

            null

        )

        setError(

            null

        )

    }

    /* ------------------------------------------------------------------------
    Return
    ------------------------------------------------------------------------ */

    return {

        selectedIntent,

        setSelectedIntent,

        maxPrice,

        setMaxPrice,

        runtime,

        setRuntime,

        loading,

        setLoading,

        error,

        setError,

        reset,

    }

}