def runtime_log(

    enabled,

    title,

    data=None,
):

    # =====================================================
    # DISABLED
    # =====================================================

    if not enabled:

        return

    # =====================================================
    # TITLE SAFETY
    # =====================================================

    title = str(title).strip()

    # =====================================================
    # TITLE
    # =====================================================

    print(

        "\n"
        f"================ {title} ================",

        flush=True,
    )

    # =====================================================
    # DATA
    # =====================================================

    if data is not None:

        print(

            data,

            flush=True,
        )