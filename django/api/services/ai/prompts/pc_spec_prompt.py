# =========================================================
# FILE:
# api/services/ai/prompts/pc_spec_prompt.py
# =========================================================


class PCSpecPrompt:

    # =====================================================
    # BUILD
    # =====================================================

    def build(

        self,

        product,

    ):

        description = (

            product.description
            or ""

        )[:2000]

        return f"""
製品情報からPCスペックを抽出せよ。

重要:

商品説明よりも
製品名を優先して解析すること。

製品名に含まれる情報は
正式スペックとして採用してよい。

==================================================
PRODUCT
==================================================

MAKER:

{product.maker}

--------------------------------------------------

NAME:

{product.name}

--------------------------------------------------

DESCRIPTION:

{description}

==================================================
OUTPUT
==================================================

返答はJSONのみ。

###JSON_START###

{{
    "cpu_model":"",
    "gpu_model":"",
    "memory_gb":0,
    "storage_gb":0,
    "display_info":"",
    "is_ai_pc":false
}}

###JSON_END###

==================================================
FIELD RULES
==================================================

cpu_model:

できるだけ正式名称で返すこと。

良い例:

Core Ultra 7 258V

Core i7-14650HX

Core Ultra 9 285H

Ryzen 7 9700X

Ryzen AI 9 HX 375

悪い例:

Core i7

Core Ultra

Ryzen 7

Ryzen AI 9

--------------------------------------------------

gpu_model:

できるだけ正式名称で返すこと。

良い例:

RTX 5070

RTX 5060

RTX 5090

RTX 4070 Laptop GPU

Radeon 890M

Intel Arc Graphics

--------------------------------------------------

memory_gb:

数値のみ。

例:

16

32

64

128

--------------------------------------------------

storage_gb:

数値のみ。

変換例:

256GB → 256

512GB → 512

1TB → 1024

2TB → 2048

4TB → 4096

--------------------------------------------------

display_info:

画面サイズや特徴。

例:

14.0 inch OLED

16 inch WQXGA

15.6 inch FHD

--------------------------------------------------

is_ai_pc:

次のいずれかに該当する場合 true

- Copilot+ PC
- Ryzen AI
- Core Ultra Series 2
- Intel AI PC
- NPU搭載
- AI PC表記あり

それ以外は false

==================================================
NAME ANALYSIS RULES
==================================================

商品名から解析できる場合は
説明文より優先すること。

例:

TM500MH-R732G1TB5060

↓

Ryzen 7
32GB
1TB
RTX 5060

--------------------------------------------------

例:

H7606WP-LPAI9642R5070W

↓

Ryzen AI 9
64GB
RTX 5070

--------------------------------------------------

例:

Core Ultra 7
RTX 5070
32GB
1TB

が商品名に含まれる場合は
積極的に採用すること。

==================================================
CHECKLIST
==================================================

回答前に確認せよ。

CPUは空欄ではないか？

GPUは空欄ではないか？

MEMORYは空欄ではないか？

STORAGEは空欄ではないか？

製品名に含まれる情報を
見落としていないか？

==================================================
RESTRICTIONS
==================================================

説明文禁止

Markdown禁止

JSON以外禁止

JSONのみ返せ
"""