class PCSpecPrompt:

    # =====================================================
    # BUILD
    # =====================================================

    def build(

        self,

        product,

    ):

        description = (

            product.description

            or ""

        )[:1000]

        return f"""
製品情報からPCスペックを抽出せよ。

返答はJSONのみ。

開始文字:

###JSON_START###

終了文字:

###JSON_END###

==================================================
PRODUCT
==================================================

MAKER:

{product.maker}

NAME:

{product.name}

DESCRIPTION:

{description}

==================================================
OUTPUT
==================================================

{{
    "cpu_model":"",
    "gpu_model":"",
    "memory_gb":0,
    "storage_gb":0,
    "display_info":"",
    "is_ai_pc":false
}}

==================================================
RULES
==================================================

cpu_model:

CPU名のみ

例:

"Core Ultra 7 258V"

"Ryzen AI 9 HX 375"

--------------------------------------------------

gpu_model:

GPU名のみ

例:

"RTX 5070"

"Radeon 890M"

--------------------------------------------------

memory_gb:

数値のみ

例:

32

--------------------------------------------------

storage_gb:

数値のみ

例:

1024

--------------------------------------------------

display_info:

ディスプレイ情報

例:

"14.0 inch OLED"

--------------------------------------------------

is_ai_pc:

true

または

false

--------------------------------------------------

説明文禁止

Markdown禁止

JSON以外禁止

必ずJSONのみ返せ
"""