/* ============================================================================
🔥 Contracts
============================================================================ */

import type {

  NavigationRuntimeResponse,

} from './contracts'

/* ============================================================================
🔥 Utils
============================================================================ */

import {

  buildEndpoint,

} from '../utils/buildEndpoint'

import {

  safeFetch,

} from '../utils/safeFetch'

/* ============================================================================
🔥 Normalize
============================================================================ */

import {

  normalizeNavigation,

} from './normalize'

/* ============================================================================
🔥 Endpoint
============================================================================ */

const NAVIGATION_ENDPOINT =
  '/pc/navigation/'

/* ============================================================================
🔥 Fetch Navigation Runtime
============================================================================ */

export async function
fetchNavigationRuntime()

: Promise<NavigationRuntimeResponse> {

  /* ========================================================================
  Endpoint
  ======================================================================== */

  const endpoint =

    buildEndpoint(
      NAVIGATION_ENDPOINT
    )

  /* ========================================================================
  Fetch
  ======================================================================== */

  const response =

    await safeFetch(
      endpoint
    )

  /* ========================================================================
  Empty Guard
  ======================================================================== */

  if (!response) {

    console.warn(
      '⚠️ Navigation Runtime Empty'
    )

    return {

      success: false,

      navigation: [],
    }
  }

  /* ========================================================================
  Observatory
  ======================================================================== */

  console.log(
    '🔥 NAVIGATION RAW RESPONSE',
    response
  )

  /* ========================================================================
  Normalize
  ======================================================================== */

  const runtime =

    normalizeNavigation(
      response
    )

  /* ========================================================================
  Runtime Observatory
  ======================================================================== */

  console.log(
    '🔥 NAVIGATION RUNTIME',
    {

      items:
        runtime.navigation?.length || 0,

      authority_version:
        runtime.authority_version,

      semantic_authority:
        runtime.semantic_authority,

      sample:
        runtime.navigation?.[0],
    }
  )

  /* ========================================================================
  Return
  ======================================================================== */

  return runtime
}

/* ============================================================================
🔥 Default Export
============================================================================ */

export default fetchNavigationRuntime