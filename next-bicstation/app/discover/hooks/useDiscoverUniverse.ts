'use client'

/* ============================================================================
🔥 React
============================================================================ */

import {

  useMemo,
  useState,

} from 'react'

/* ============================================================================
🔥 Types
============================================================================ */

import type {

  NavigationItem,
  Universe,

} from '../types/discover'

/* ============================================================================
🔥 Utils
============================================================================ */

import {

  groupNavigation,

} from '../utils/groupNavigation'

/* ============================================================================
🔥 Hook
============================================================================ */

export function useDiscoverUniverse(

  universes: Universe[],

  navigation: NavigationItem[],

) {

  /* ==========================================================================
  🔥 Grouped Navigation
  ========================================================================== */

  const groupedNavigation =

    useMemo(

      () =>

        groupNavigation(
          navigation
        ),

      [navigation]

    )

  /* ==========================================================================
  🔥 Default Universe
  ========================================================================== */

  const defaultUniverse =

    universes?.[0]
      ?.universe_slug

      ||

    ''

  /* ==========================================================================
  🔥 Active Universe
  ========================================================================== */

  const [

    activeUniverse,

    setActiveUniverse,

  ] = useState(
    defaultUniverse
  )

  /* ==========================================================================
  🔥 Active Cards
  ========================================================================== */

  const activeNavigation =

    groupedNavigation[
      activeUniverse
    ]

    ||

    []

  /* ==========================================================================
  🔥 Return
  ========================================================================== */

  return {

    activeUniverse,

    setActiveUniverse,

    groupedNavigation,

    activeNavigation,

  }

}

/* ============================================================================
🔥 Default Export
============================================================================ */

export default useDiscoverUniverse