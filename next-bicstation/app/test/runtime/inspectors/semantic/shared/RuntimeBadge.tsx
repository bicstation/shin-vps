/* ============================================================================
🔥 Props
============================================================================ */

type RuntimeBadgeProps = {

  label: string

  value?: string

  variant?:
    | 'default'
    | 'success'
    | 'warning'
    | 'danger'
    | 'semantic'
    | 'topology'
    | 'transport'
}

/* ============================================================================
🔥 Variant Styles
============================================================================ */

function resolveVariantStyle(

  variant: RuntimeBadgeProps['variant']

): string {

  switch (variant) {

    /* ================================================================
    ✅ Success
    ================================================================ */

    case 'success':

      return (

        'border-emerald-900 ' +
        'bg-emerald-950/40 ' +
        'text-emerald-400'
      )

    /* ================================================================
    ⚠️ Warning
    ================================================================ */

    case 'warning':

      return (

        'border-yellow-900 ' +
        'bg-yellow-950/40 ' +
        'text-yellow-400'
      )

    /* ================================================================
    ❌ Danger
    ================================================================ */

    case 'danger':

      return (

        'border-red-900 ' +
        'bg-red-950/40 ' +
        'text-red-400'
      )

    /* ================================================================
    🧠 Semantic
    ================================================================ */

    case 'semantic':

      return (

        'border-fuchsia-900 ' +
        'bg-fuchsia-950/40 ' +
        'text-fuchsia-400'
      )

    /* ================================================================
    🌐 Topology
    ================================================================ */

    case 'topology':

      return (

        'border-cyan-900 ' +
        'bg-cyan-950/40 ' +
        'text-cyan-400'
      )

    /* ================================================================
    🚚 Transport
    ================================================================ */

    case 'transport':

      return (

        'border-blue-900 ' +
        'bg-blue-950/40 ' +
        'text-blue-400'
      )

    /* ================================================================
    ⚙️ Default
    ================================================================ */

    default:

      return (

        'border-zinc-800 ' +
        'bg-zinc-900 ' +
        'text-zinc-300'
      )
  }
}

/* ============================================================================
🔥 Runtime Badge
============================================================================ */

export default function RuntimeBadge({

  label,

  value,

  variant = 'default',

}: RuntimeBadgeProps) {

  /* ==========================================================================
  🔥 Style
  ========================================================================== */

  const variantStyle =

    resolveVariantStyle(
      variant
    )

  /* ==========================================================================
  🔥 Display Value
  ========================================================================== */

  const displayValue =

    value || '-'

  /* ==========================================================================
  🔥 Runtime Debug
  ========================================================================== */

  console.log(

    '🏷️ RUNTIME BADGE',

    {

      label,

      value:

        displayValue,

      variant,
    }
  )

  /* ==========================================================================
  🔥 Render
  ========================================================================== */

  return (

    <div

      className={

        [
          'inline-flex',
          'items-center',
          'gap-2',
          'rounded-full',
          'border',
          'px-3',
          'py-1',
          'text-[10px]',
          'uppercase',
          'tracking-[0.2em]',
          variantStyle,
        ].join(' ')
      }

    >

      {/* ================================================================
      🔥 Label
      ================================================================ */}

      <span className="opacity-70">

        {label}

      </span>

      {/* ================================================================
      🔥 Divider
      ================================================================ */}

      <span className="opacity-30">

        •

      </span>

      {/* ================================================================
      🔥 Value
      ================================================================ */}

      <span className="font-semibold">

        {displayValue}

      </span>

    </div>
  )
}