# --------------------------------------------------
# Blank
# --------------------------------------------------

def blank(
    lines,
):
    lines.append("")

# --------------------------------------------------
# Heading
# --------------------------------------------------

def heading(
    lines,
    title,
):
    lines.append(f"# {title}")
    blank(lines)

# --------------------------------------------------
# Section
# --------------------------------------------------

def section(
    lines,
    title,
):
    lines.append(title)

# --------------------------------------------------
# Item
# --------------------------------------------------

def item(
    lines,
    text,
):
    lines.append(
        f"├── {text}"
    )

# --------------------------------------------------
# Last Item
# --------------------------------------------------

def last_item(
    lines,
    text,
):
    lines.append(
        f"└── {text}"
    )

# --------------------------------------------------
# Child Item
# --------------------------------------------------

def child_item(
    lines,
    text,
):
    lines.append(
        f"│   ├── {text}"
    )

# --------------------------------------------------
# Last Child Item
# --------------------------------------------------

def last_child_item(
    lines,
    text,
):
    lines.append(
        f"│   └── {text}"
    )

# --------------------------------------------------
# Value
# --------------------------------------------------

def value(
    lines,
    text,
):
    lines.append(
        f"│   │   {text}"
    )

# --------------------------------------------------
# Text
# --------------------------------------------------

def text(
    lines,
    value,
):
    lines.append(
        value
    )

# --------------------------------------------------
# Divider
# --------------------------------------------------

def divider(
    lines,
):
    blank(lines)
    lines.append(
        "---"
    )

    blank(lines)