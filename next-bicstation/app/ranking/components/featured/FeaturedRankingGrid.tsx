import FeaturedRankingCard
    from './FeaturedRankingCard'

/* ============================================================================
🔥 Types
============================================================================ */

import type {

    RankingProduct,

} from '@/shared/lib/api/django/pc/ranking/contracts'

/* ============================================================================
🔥 Styles
============================================================================ */

import styles
    from '../../styles/featured/featured-grid.module.css'

/* ============================================================================
🔥 Props
============================================================================ */

type Props = {

    items: RankingProduct[]

}

/* ============================================================================
🔥 Featured Ranking Grid
============================================================================ */

export default function FeaturedRankingGrid({

    items,

}: Props) {

    /* =========================================================================
    🔥 Featured Product
    ========================================================================= */

    const featuredProduct =

        items[0]

    /* =========================================================================
    🔥 Empty
    ========================================================================= */

    if (

        !featuredProduct

    ) {

        return null

    }

    /* =========================================================================
    🔥 Render
    ========================================================================= */

    return (

        <section
            className={styles.grid}
        >

            <FeaturedRankingCard

                product={featuredProduct}

                rank={1}

            />

        </section>

    )

}