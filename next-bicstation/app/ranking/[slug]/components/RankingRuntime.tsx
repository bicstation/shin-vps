'use client'

/* ============================================================================
🔥 Contracts
============================================================================ */

import type {

    SemanticRankingRuntime,

} from '../types/contracts'

/* ============================================================================
🔥 Components
============================================================================ */

import {

    RankingHero,

    RankingBreadcrumbs,

    FlagshipCard,

    ComparisonGrid,

    RankingList,

    RankingFAQ,

    RankingContinuation,

} from './'

/* ============================================================================
🔥 Props
============================================================================ */

type Props = {

    runtime: SemanticRankingRuntime

}

/* ============================================================================
🔥 Ranking Runtime
============================================================================ */

export default function RankingRuntime({

    runtime,

}: Props) {

    /* =========================================================================
    🔥 Runtime
    ========================================================================= */
   
    const {

        products = [],

        breadcrumbs = [],

        faq = [],

    } = runtime

    /* =========================================================================
    🔥 Ranking Structure
    ========================================================================= */


    const flagship =
        products[0]

    const comparisonProducts =
        products.slice(1, 4)

    const rankingProducts =
        products.slice(4)


    /* =========================================================================
    🔥 Render
    ========================================================================= */

    return (

        <main>

            {/* ==========================================================
            Hero
            ========================================================== */}

            <RankingHero

                runtime={runtime}

                totalProducts={products.length}

            />

            {/* ==========================================================
            Breadcrumbs
            ========================================================== */}

            <RankingBreadcrumbs

                breadcrumbs={breadcrumbs}

            />

            {/* ==========================================================
            Flagship
            ========================================================== */}

            

            {flagship && (

                <FlagshipCard

                    product={flagship}

                    rank={1}

                />

            )}

           

            {/* ==========================================================
            Comparison
            ========================================================== */}

            

            {comparisonProducts.length > 0 && (

                <ComparisonGrid

                    products={comparisonProducts}

                />

            )}

           

            {/* ==========================================================
            Ranking
            ========================================================== */}

            

            {rankingProducts.length > 0 && (

                <RankingList

                    products={rankingProducts}

                    startRank={5}

                />

            )}

           

            {/* ==========================================================
            FAQ
            ========================================================== */}

            

            {faq.length > 0 && (

                <RankingFAQ

                    faq={faq}

                />

            )}

           

            {/* ==========================================================
            Continuation
            ========================================================== */}

            

            <RankingContinuation />

           

        </main>

    )

}