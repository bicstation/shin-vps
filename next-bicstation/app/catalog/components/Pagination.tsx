'use client'

/* ============================================================================
🔥 Next
============================================================================ */

import {

    useRouter,

    useSearchParams,

} from 'next/navigation'

/* ============================================================================
🔥 Styles
============================================================================ */

import styles
    from '../styles/catalog.module.css'

/* ============================================================================
🔥 Props
============================================================================ */

type Props = {

    page: number

    page_size: number

    has_next: boolean

}

/* ============================================================================
🔥 Pagination
============================================================================ */

export default function Pagination({

    page,

    page_size,

    has_next,

}: Props) {

    /* ==========================================================================
    🔥 Router
    ========================================================================== */

    const router =
        useRouter()

    const searchParams =
        useSearchParams()

    /* ==========================================================================
    🔥 Navigate
    ========================================================================== */

    function moveTo(

        nextPage: number,

    ) {

        const params =

            new URLSearchParams(

                searchParams.toString()

            )

        params.set(

            'page',

            String(nextPage)

        )

        router.push(

            `/catalog?${params.toString()}`

        )

    }

    /* ==========================================================================
    🔥 Render
    ========================================================================== */

    return (

        <nav
            className={
                styles.pagination
            }
        >

            {/* ==========================================================
      Previous
      ========================================================== */}

            <button

                className={
                    styles.paginationButton
                }

                disabled={
                    page <= 1
                }

                onClick={() =>

                    moveTo(

                        page - 1

                    )

                }

            >

                ← 前へ

            </button>

            {/* ==========================================================
      Current
      ========================================================== */}

            <div
                className={
                    styles.paginationInfo
                }
            >

                Page {page}

                <span>

                    {' '}

                    ·

                    {' '}

                    {page_size}件表示

                </span>

            </div>

            {/* ==========================================================
      Next
      ========================================================== */}

            <button

                className={
                    styles.paginationButton
                }

                disabled={
                    !has_next
                }

                onClick={() =>

                    moveTo(

                        page + 1

                    )

                }

            >

                次へ →

            </button>

        </nav>

    )

}