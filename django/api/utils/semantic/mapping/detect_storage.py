# =========================================================
# STORAGE ATTRIBUTE DETECTION
# =========================================================

def detect_storage_attr(
    specs,
):

    slugs = []

    storage = specs.get(
        "storage_gb",
        0
    ) or 0

    # =====================================================
    # 4TB+
    # =====================================================

    if storage >= 4096:

        slugs.append(
            "storage-4tb"
        )

    # =====================================================
    # 2TB+
    # =====================================================

    elif storage >= 2048:

        slugs.append(
            "storage-2tb"
        )

    # =====================================================
    # 1TB+
    # =====================================================

    elif storage >= 1024:

        slugs.append(
            "storage-1tb"
        )

    # =====================================================
    # 512GB+
    # =====================================================

    elif storage >= 512:

        slugs.append(
            "storage-512gb"
        )

    # =====================================================
    # 256GB+
    # =====================================================

    elif storage >= 256:

        slugs.append(
            "storage-256gb"
        )

    # =====================================================
    # LOW STORAGE
    # =====================================================

    else:

        slugs.append(
            "storage-basic"
        )

    return slugs