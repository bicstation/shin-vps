def build_paragraphs(text=""):

    if not text:
        return []

    text = text.strip()

    # ------------------------------------------------------------------------
    # normalize
    # ------------------------------------------------------------------------

    text = text.replace("\r", "")

    # ------------------------------------------------------------------------
    # split
    # ------------------------------------------------------------------------

    paragraphs = []

    for line in text.split("\n"):

        line = line.strip()

        if not line:
            continue

        paragraphs.append(line)

    # ------------------------------------------------------------------------
    # fallback
    # ------------------------------------------------------------------------

    if not paragraphs:

        paragraphs = [text]

    return paragraphs