# ============================================================================

from satellite_ops.runtime.rss.parser_factory import (
    get_parser,
)

from satellite_ops.runtime.rss.extractor_factory import (
    get_extractor,
)


# ============================================================================
# Normalize Article
# ============================================================================

def normalize_article(
    rss_item: dict,
) -> dict:
    """
    Normalize RSS item into unified article object.
    """

    # ========================================================================
    # RSS Base
    # ========================================================================

    url = rss_item.get(
        "link",
        "",
    )

    title = rss_item.get(
        "title",
        "",
    )

    summary = rss_item.get(
        "summary",
        "",
    )

    category = rss_item.get(
        "category",
        "",
    )

    # ========================================================================
    # Select Parser
    # ========================================================================

    parser = get_parser(
        url
    )

    # ========================================================================
    # Parse Body
    # ========================================================================

    body = parser.parse(
        url
    )

    # ========================================================================
    # Fetch HTML
    # ========================================================================

    html = parser.fetch_html(
        url
    )

    # ========================================================================
    # Select Extractor
    # ========================================================================

    extractor = get_extractor(

        url,

        category,
    )

    # ========================================================================
    # Extract Image
    # ========================================================================

    image_url = extractor.extract(
        html
    )

    # ========================================================================
    # Runtime Observability
    # ========================================================================

    parser_name = getattr(
        parser,
        "__name__",
        "UnknownParser",
    )

    extractor_name = getattr(
        extractor,
        "__name__",
        "UnknownExtractor",
    )

    # ========================================================================
    # Normalized Object
    # ========================================================================

    return {

        # --------------------------------------------------------------------
        # Core
        # --------------------------------------------------------------------

        "title": title,

        "summary": summary,

        "body": body,

        "image_url": image_url,

        # --------------------------------------------------------------------
        # Metadata
        # --------------------------------------------------------------------

        "source": url,

        "published_at": rss_item.get(
            "published",
            "",
        ),

        "url": url,
        
        "source_name": rss_item.get(
            "source_name",
                "",
        ),

        # --------------------------------------------------------------------
        # Runtime Observability
        # --------------------------------------------------------------------

        "parser": parser_name,

        "extractor": extractor_name,
    }